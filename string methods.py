name= "josephatomondi"
print(len(name)) #length of the word Josephat
print(name.find("o")) #finding where character "o" is, we start with 0,1,2,3....
print(name.capitalize()) #this will capitalize the character 'J' if it was written in lowercase
print(name.upper()) #Uppecase
print(name.lower()) #lowercase
print(name.isdigit()) #this are letters per se hence the answer 'False'
print(name.isalpha()) # True because they are alphabets
print(name.count("o")) #Counts the no. of 'o'
print(name.replace("o", "a")) #replaces every letter 'o' with 'a'
